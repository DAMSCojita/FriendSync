package org.stefancojita.friendsync;

public class Asistente {

    private String id;
    private String email;

    public Asistente() {}

    public Asistente(String id, String email) {
        this.id = id;
        this.email = email;
    }

    public String getId() { return id; }
    public String getEmail() { return email; }

}

